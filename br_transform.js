function convertRawToBR(scale, raw) {
    if (!brTable[scale]) return 0;
    // Check if raw score exceeds table range
    let rawKeys = Object.keys(brTable[scale]).map(Number);
    let maxRaw = Math.max(...rawKeys);
    if (raw >= maxRaw) return brTable[scale][maxRaw];
    return brTable[scale][raw] !== undefined ? brTable[scale][raw] : 0;
}
